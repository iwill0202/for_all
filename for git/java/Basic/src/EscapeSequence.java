public class EscapeSequence {
	
	public static void main(String[] args) {
		System.out.println("Hello 是World!!!");
		System.out.println("Hello只是World!!!");
		System.out.print("Hello只是World!!!\n");
		System.out.print("Hello\n只是World!!!\n");
		System.out.print("Hello\t只是World!!!\n");
		System.out.print("Hello\\world!!!\n");
		System.out.print("I'm a teacher!\n");
		System.out.print("I\'m a teacher!\n");
		System.out.print("I\"m a teacher!\n");
	}
	
}
