
public class Literal {

	public static void main(String[] args) {
		float f = 3.0F;
		System.out.println(f);
	//	int n = 0X144;
		int n = 0144;
		System.out.println(n);
		double d = 3.45E12;
		System.out.println(d);
	//	char c = 'A';
	//	char c = 65;
		char c = '\u0041';
		System.out.println(c);
	}
	
}
