package com.iii.classPractice;
public class P52_Switch {
	public static void main(String[] args) {
		System.out.println("Enter an integer(1~4):");
		int num=3;
		switch(num){
			case 1:System.out.println("intput 1");break;
			case 2:System.out.println("intput 2");break;
			case 3:System.out.println("intput 3");break;
			default:System.out.println("input error");
		}
		System.out.println("Thank you!");
		
	}

}
